global using u8 = byte;    // 0 to 255
global using i16 = short;  // -32,768 to 32,767
global using u16 = ushort; // 0 to 65,535
global using i32 = int;    // -2,147,483,648 to 2,147,483,647
global using u32 = uint;   // 0 to 4,294,967,295
global using i64 = long;   // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
global using u64 = ulong;  // 0 to 18,446,744,073,709,551,615

global using ref8 = System.Span<byte>;
